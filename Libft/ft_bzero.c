#include <stdlib.h>
#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*buffer;
	size_t	i;

	buffer = s;
	i = 0;
	while (i < n)
	{
		buffer[i] = '\0';
		i ++;
	}
}
